package vn.hoidanit.laptopshop.domain.DTO;

public class LocationDTO {
    private String name;
    private String phone;
    private String detailAdd;
    private String districtAdd;
    private String provinceAdd;
    private String villageAdd;
    private String note;
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getPhone() {
        return phone;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }
    public String getDetailAdd() {
        return detailAdd;
    }
    public void setDetailAdd(String detailAdd) {
        this.detailAdd = detailAdd;
    }
    public String getDistrictAdd() {
        return districtAdd;
    }
    public void setDistrictAdd(String districtAdd) {
        this.districtAdd = districtAdd;
    }
    public String getProvinceAdd() {
        return provinceAdd;
    }
    public void setProvinceAdd(String provinceAdd) {
        this.provinceAdd = provinceAdd;
    }
    public String getVillageAdd() {
        return villageAdd;
    }
    public void setVillageAdd(String villageAdd) {
        this.villageAdd = villageAdd;
    }
    public String getNote() {
        return note;
    }
    public void setNote(String note) {
        this.note = note;
    }
    
}
